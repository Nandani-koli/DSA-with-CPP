#include <iostream>
#include <cmath>

using namespace std;
int main()
{
    int x = 10;
    int y = 20;
    cout<<min(x,y)<<"\n";
    cout<<max(x,y)<<"\n";

    //sqrt // round // log functions found in <cmath> header file
    //Basic Functions in <cmath> File 

    cout<<sqrt(4)<<"\n";

    cout<<floor(9.009)<<"\n";
    cout<<round(9.89)<<"\n";
    cout<<ceil(9.0)<<"\n";

    cout<<log(10)<<"\n";
    cout<<abs(-9)<<"\n";
    cout<<acos(0.75)<<"\n";
    cout<<cbrt(125)<<"\n";
    cout<<exp(2)<<"\n";
    cout<<fabs(-8.000)<<"\n";  // absolute value of floating number
    cout<<pow(3,2);

    return 0;
}